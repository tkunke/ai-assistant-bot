"use client";

import React, { useState } from "react";
import { AiOutlineSend } from 'react-icons/ai';

export default function CinetechShotComp() {
  const [imageUrl, setImageUrl] = useState("");
  const [isLoading, setIsLoading] = useState(false);
  const [prompt, setPrompt] = useState('');

  async function generateImage(e) {
    e.preventDefault();
    setIsLoading(true);

    try {
      // Post user prompt to image generator and return image
      const response = await fetch('/api/image-generator', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          content: prompt,
        })
      });

      if (!response.ok) {
        throw new Error(`HTTP error! Status: ${response.status}`);
      }

      const data = await response.json();
      setImageUrl(data.url);
    } catch (error) {
      console.error("Error generating image:", error);
    } finally {
      setIsLoading(false);
      setPrompt(''); // Clear the prompt input field
    }
  }

  function handlePromptChange(e) {
    setPrompt(e.target.value);
  }

  return (
    <div className="flex flex-col relative py-10">
      {imageUrl ? (
        <img src={imageUrl} alt="Generated by OpenAI" />
      ) : (
        <p>What can I generate for you?</p>
      )}
      <footer className="footer">
        <div className="mx-auto mb-20 max-w-custom text-center p-8 rounded-lg xs:p-2 md:p-4 md:py-2">
          <form onSubmit={generateImage} className="m-2 flex flex-col md:flex-row items-center">
            <input
              disabled={isLoading}
              className="border rounded w-full md:w-auto py-2 px-3 text-gray-700 mb-2 md:mb-0 md:mr-2"
              onChange={handlePromptChange}
              value={prompt}
              placeholder="Type your query here..."
              style={{ minWidth: '200px', flexGrow: 1 }}
              backgroundcolor="whitesmoke"
            />
            {isLoading ? (
              <button disabled className="bg-black text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline mb-2 md:mb-0 md:ml-2">
                <CinetechSpinner />
              </button>
            ) : (
              <button disabled={prompt.length === 0} className="bg-black hover:bg-gray-800 text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline mb-2 md:mb-0 md:ml-2">
                <AiOutlineSend />
              </button>
            )}
          </form>
        </div>
      </footer>
    </div>
  );
}

